fn generate_syscalls() {
    println!("cargo:rerun-if-changed=umlang-syscalls.txt");

    let text =
        fs::read_to_string("umlang-syscalls.txt").expect("failed to read umlang-syscalls.txt");
    let mut generated = String::from("// @generated by build.rs from umlang-syscalls.txt\n");

    for raw_line in text.lines() {
        let line = raw_line.trim();
        if line.is_empty() || line.starts_with('#') {
            continue;
        }
        let (name, value) = line
            .split_once('=')
            .unwrap_or_else(|| panic!("invalid syscall line: {line}"));
        let name = name.trim();
        let value = value.trim();
        assert!(
            name.starts_with("SYS_")
                && name
                    .chars()
                    .all(|ch| ch.is_ascii_uppercase() || ch == '_' || ch.is_ascii_digit()),
            "invalid syscall name: {name}"
        );
        let value: i32 = value
            .parse()
            .unwrap_or_else(|_| panic!("invalid syscall value for {name}: {value}"));
        generated.push_str(&format!("pub const {name}: i32 = {value};\n"));
    }

    let out_path = PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set"))
        .join("generated_syscalls.rs");
    fs::write(out_path, generated).expect("failed to write generated syscalls");
}

fn generate_keymap() {
    println!("cargo:rerun-if-changed=umlang-keycodes.txt");
    println!("cargo:rerun-if-changed=umlang-keymap.txt");

    let key_codes = read_keycodes();
    let text = fs::read_to_string("umlang-keymap.txt").expect("failed to read umlang-keymap.txt");
    let mut generated =
        String::from("// @generated by build.rs from umlang-keycodes.txt and umlang-keymap.txt\n");
    generated.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq)]\n");
    generated.push_str("pub struct KeyBinding {\n");
    generated.push_str("    pub action: i32,\n");
    generated.push_str("    pub key_name: &'static str,\n");
    generated.push_str("    pub code: i32,\n");
    generated.push_str("}\n\n");

    let mut actions = BTreeSet::new();
    let mut bindings = Vec::new();

    for raw_line in text.lines() {
        let line = strip_comment(raw_line);
        if line.is_empty() {
            continue;
        }
        let (action_name, rest) = line
            .split_once('=')
            .unwrap_or_else(|| panic!("invalid keymap line: {line}"));
        let action_name = action_name.trim();
        validate_const_name(action_name, "ACTION_");
        assert!(
            actions.insert(action_name.to_string()),
            "duplicate action name: {action_name}"
        );

        let fields: Vec<_> = rest.split(',').map(str::trim).collect();
        assert!(
            fields.len() == 2,
            "invalid keymap value for {action_name}: {rest}"
        );
        let action: i32 = fields[0]
            .parse()
            .unwrap_or_else(|_| panic!("invalid action id for {action_name}: {}", fields[0]));
        let key_name = fields[1];
        validate_const_name(key_name, "KEY_");
        let code = *key_codes
            .get(key_name)
            .unwrap_or_else(|| panic!("undefined key constant for {action_name}: {key_name}"));
        bindings.push((action_name.to_string(), action, key_name.to_string(), code));
    }

    for (action_name, action, _, _) in &bindings {
        generated.push_str(&format!("pub const {action_name}: i32 = {action};\n"));
    }
    generated.push('\n');

    for (key_name, code) in &key_codes {
        generated.push_str(&format!("pub const {key_name}: i32 = {code};\n"));
    }
    generated.push_str("\npub const KEY_BINDINGS: &[KeyBinding] = &[\n");

    for (action_name, _, key_name, _) in &bindings {
        generated.push_str(&format!(
            "    KeyBinding {{ action: {action_name}, key_name: \"{key_name}\", code: {key_name} }},\n"
        ));
    }
    generated.push_str("];\n");

    let out_path =
        PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set")).join("generated_keymap.rs");
    fs::write(out_path, generated).expect("failed to write generated keymap");
}

fn read_keycodes() -> BTreeMap<String, i32> {
    let text =
        fs::read_to_string("umlang-keycodes.txt").expect("failed to read umlang-keycodes.txt");
    let mut key_codes = BTreeMap::new();
    for raw_line in text.lines() {
        let line = strip_comment(raw_line);
        if line.is_empty() {
            continue;
        }
        let (key_name, value) = line
            .split_once('=')
            .unwrap_or_else(|| panic!("invalid keycode line: {line}"));
        let key_name = key_name.trim();
        validate_const_name(key_name, "KEY_");
        let code: i32 = value
            .trim()
            .parse()
            .unwrap_or_else(|_| panic!("invalid keycode value for {key_name}: {value}"));
        assert!(
            key_codes.insert(key_name.to_string(), code).is_none(),
            "duplicate key constant: {key_name}"
        );
    }
    key_codes
}

fn generate_assets() {
    println!("cargo:rerun-if-changed=umlang-assets.txt");

    let values = read_key_values("umlang-assets.txt");
    let mut generated = String::from("// @generated by build.rs from umlang-assets.txt\n");

    let settings_profile = required_value(&values, "settings_profile");
    let sprite_texture = required_value(&values, "sprite_texture");
    let bgm_audio = required_value(&values, "bgm_audio");

    generated.push_str(&format!(
        "pub const SETTINGS_PROFILE: &str = {:?};\n",
        settings_profile
    ));
    generated.push_str(&format!(
        "pub const SPRITE_TEXTURE: &str = {:?};\n",
        sprite_texture
    ));
    generated.push_str(&format!("pub const BGM_AUDIO: &str = {:?};\n", bgm_audio));

    for key in [
        "sprite_texture_slot",
        "bgm_audio_slot",
        "sfx_base_slot",
        "sfx_sound_stride",
        "sfx_mode_stride",
        "sfx_mono_mode",
        "sfx_stereo_mode",
        "sfx_left_side_code",
        "sfx_center_side_code",
        "sfx_right_side_code",
    ] {
        let const_name = key.to_ascii_uppercase();
        let value = required_i32(&values, key);
        generated.push_str(&format!("pub const {const_name}: i32 = {value};\n"));
    }

    let waves = required_i32_list(&values, "sfx_waves");
    generated.push_str("\npub const SFX_WAVES: &[i32] = &[");
    for (index, wave) in waves.iter().enumerate() {
        if index > 0 {
            generated.push_str(", ");
        }
        generated.push_str(&wave.to_string());
    }
    generated.push_str("];\n");

    let out_path =
        PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set")).join("generated_assets.rs");
    fs::write(out_path, generated).expect("failed to write generated assets");
}

fn generate_palette() {
    println!("cargo:rerun-if-changed=umlang-palette.txt");

    let values = read_key_values("umlang-palette.txt");
    let mut colors = Vec::new();
    for (key, value) in values {
        let color_id: i32 = key
            .parse()
            .unwrap_or_else(|_| panic!("invalid palette color id: {key}"));
        let rgba = parse_i32_list_value("palette rgba", &value);
        assert!(
            rgba.len() == 4,
            "palette color {color_id} must have exactly 4 rgba values"
        );
        colors.push([color_id, rgba[0], rgba[1], rgba[2], rgba[3]]);
    }
    colors.sort_by_key(|color| color[0]);

    let mut generated = String::from("// @generated by build.rs from umlang-palette.txt\n");
    generated.push_str("pub const PALETTE: &[[i32; 5]] = &[\n");
    for [color_id, red, green, blue, alpha] in colors {
        generated.push_str(&format!(
            "    [{color_id}, {red}, {green}, {blue}, {alpha}],\n"
        ));
    }
    generated.push_str("];\n");

    let out_path = PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set"))
        .join("generated_palette.rs");
    fs::write(out_path, generated).expect("failed to write generated palette");
}

fn generate_settings() {
    println!("cargo:rerun-if-changed=umlang-settings.txt");

    let values = read_key_values("umlang-settings.txt");
    let mut settings = Vec::new();
    for (name, value) in values {
        let fields: Vec<_> = value.split(',').map(str::trim).collect();
        assert!(
            fields.len() == 3,
            "setting {name} must be key,default,allowed_values"
        );
        let key = fields[0]
            .parse::<i32>()
            .unwrap_or_else(|_| panic!("invalid setting key for {name}: {}", fields[0]));
        let default = fields[1]
            .parse::<i32>()
            .unwrap_or_else(|_| panic!("invalid setting default for {name}: {}", fields[1]));
        let allowed =
            parse_i32_delimited_value(&format!("setting {name} allowed values"), fields[2], '|');
        assert!(
            allowed.contains(&default),
            "setting {name} default must be in allowed values"
        );
        settings.push((name, key, default, allowed));
    }
    settings.sort_by_key(|(_, key, _, _)| *key);

    let mut generated = String::from("// @generated by build.rs from umlang-settings.txt\n");
    for (name, key, default, allowed) in settings {
        let const_name = name.to_ascii_uppercase();
        generated.push_str(&format!("pub const SETTING_{const_name}: i32 = {key};\n"));
        generated.push_str(&format!(
            "pub const DEFAULT_{const_name}: i32 = {default};\n"
        ));
        generated.push_str(&format!("pub const {const_name}_VALUES: &[i32] = &["));
        for (index, value) in allowed.iter().enumerate() {
            if index > 0 {
                generated.push_str(", ");
            }
            generated.push_str(&value.to_string());
        }
        generated.push_str("];\n");
    }

    let out_path = PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set"))
        .join("generated_settings.rs");
    fs::write(out_path, generated).expect("failed to write generated settings");
}

fn generate_vars() {
    println!("cargo:rerun-if-changed=umlang-vars.txt");

    let text = fs::read_to_string("umlang-vars.txt").expect("failed to read umlang-vars.txt");
    let mut vars = Vec::new();
    let mut seen_names = BTreeSet::new();
    let mut seen_slots = BTreeSet::new();

    for raw_line in text.lines() {
        let line = strip_comment(raw_line);
        if line.is_empty() {
            continue;
        }
        let (name, value) = line
            .split_once('=')
            .unwrap_or_else(|| panic!("invalid variable slot line: {line}"));
        let name = name.trim();
        validate_const_name(name, "");
        let slot = value
            .trim()
            .parse::<usize>()
            .unwrap_or_else(|_| panic!("invalid variable slot for {name}: {value}"));
        assert!(
            slot > 1,
            "variable slot {name} must not overlap syscall argument slots"
        );
        assert!(
            seen_names.insert(name.to_string()),
            "duplicate variable name: {name}"
        );
        assert!(seen_slots.insert(slot), "duplicate variable slot: {slot}");
        vars.push((name.to_string(), slot));
    }
    vars.sort_by_key(|(_, slot)| *slot);

    let mut generated = String::from("// @generated by build.rs from umlang-vars.txt\n");
    for (name, slot) in vars {
        generated.push_str(&format!("pub const {name}: usize = {slot};\n"));
    }

    let out_path =
        PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set")).join("generated_vars.rs");
    fs::write(out_path, generated).expect("failed to write generated vars");
}

fn generate_sfx() {
    println!("cargo:rerun-if-changed=umlang-sfx.txt");
    println!("cargo:rerun-if-changed=umlang-vars.txt");
    println!("cargo:rerun-if-changed=umlang-assets.txt");

    let valid_vars = read_var_name_set();
    let sfx_sound_count =
        required_i32_list(&read_key_values("umlang-assets.txt"), "sfx_waves").len();
    let text = fs::read_to_string("umlang-sfx.txt").expect("failed to read umlang-sfx.txt");
    let mut round_events = Vec::new();
    let mut ui_events = Vec::new();
    let mut jump_sound = None;
    let mut seen_flags = BTreeSet::new();

    for raw_line in text.lines() {
        let line = strip_comment(raw_line);
        if line.is_empty() {
            continue;
        }
        let (key, value) = line
            .split_once('=')
            .unwrap_or_else(|| panic!("invalid sfx line: {line}"));
        let key = key.trim();
        assert!(
            key.chars()
                .all(|ch| ch.is_ascii_lowercase() || ch.is_ascii_digit() || ch == '_'),
            "invalid sfx key: {key}"
        );

        if key.starts_with("round_event_") {
            let event = parse_sfx_event(key, value, &valid_vars, sfx_sound_count);
            assert!(
                seen_flags.insert(event.0.clone()),
                "duplicate sfx flag: {}",
                event.0
            );
            round_events.push(event);
        } else if key.starts_with("ui_event_") {
            let event = parse_sfx_event(key, value, &valid_vars, sfx_sound_count);
            assert!(
                seen_flags.insert(event.0.clone()),
                "duplicate sfx flag: {}",
                event.0
            );
            ui_events.push(event);
        } else if key == "jump_sound" {
            let fields: Vec<_> = value.split(',').map(str::trim).collect();
            assert!(fields.len() == 2, "jump_sound must be sound_id,side");
            let sound_id = parse_sfx_sound_id(key, fields[0], sfx_sound_count);
            let side = parse_fixed_sfx_side(key, fields[1]);
            assert!(
                jump_sound.replace((sound_id, side)).is_none(),
                "duplicate jump_sound"
            );
        } else {
            panic!("unknown sfx key: {key}");
        }
    }

    assert!(
        !round_events.is_empty(),
        "umlang-sfx.txt must define round SFX events"
    );
    assert!(
        !ui_events.is_empty(),
        "umlang-sfx.txt must define UI SFX events"
    );
    let (jump_sound_id, jump_side) = jump_sound.expect("umlang-sfx.txt must define jump_sound");

    let mut generated = String::from("// @generated by build.rs from umlang-sfx.txt\n");
    generated.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq)]\n");
    generated.push_str("pub enum SfxSide {\n");
    generated.push_str("    Fixed(i32),\n");
    generated.push_str("    BallX,\n");
    generated.push_str("}\n\n");
    generated.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq)]\n");
    generated.push_str("pub struct SfxEvent {\n");
    generated.push_str("    pub flag_name: &'static str,\n");
    generated.push_str("    pub sound_id: i32,\n");
    generated.push_str("    pub side: SfxSide,\n");
    generated.push_str("}\n\n");
    generated.push_str(&format!(
        "pub const JUMP_SOUND_ID: i32 = {jump_sound_id};\n"
    ));
    generated.push_str(&format!("pub const JUMP_SIDE: i32 = {jump_side};\n\n"));

    write_sfx_events(&mut generated, "ROUND_SFX_EVENTS", &round_events);
    generated.push('\n');
    write_sfx_events(&mut generated, "UI_SFX_EVENTS", &ui_events);

    let out_path =
        PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set")).join("generated_sfx.rs");
    fs::write(out_path, generated).expect("failed to write generated sfx constants");
}

