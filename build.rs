fn generate_sprites() {
    println!("cargo:rerun-if-changed=umlang-sprites.txt");

    let text = fs::read_to_string("umlang-sprites.txt").expect("failed to read umlang-sprites.txt");
    let mut player_frames = BTreeMap::new();
    let mut ball_frames = BTreeMap::new();

    for raw_line in text.lines() {
        let line = strip_comment(raw_line);
        if line.is_empty() {
            continue;
        }
        let (name, value) = line
            .split_once('=')
            .unwrap_or_else(|| panic!("invalid sprite frame line: {line}"));
        let name = name.trim();
        validate_const_name(name, "");
        let rect = parse_sprite_rect(name, value.trim());

        if let Some(index) = name.strip_prefix("PLAYER_FRAME_") {
            let index = parse_frame_index("PLAYER_FRAME", index);
            assert!(
                player_frames.insert(index, rect).is_none(),
                "duplicate player frame index: {index}"
            );
        } else if let Some(index) = name.strip_prefix("BALL_FRAME_") {
            let index = parse_frame_index("BALL_FRAME", index);
            assert!(
                ball_frames.insert(index, rect).is_none(),
                "duplicate ball frame index: {index}"
            );
        } else {
            panic!("unknown sprite frame name: {name}");
        }
    }

    let player_frames = contiguous_frames("PLAYER_FRAME", player_frames);
    let ball_frames = contiguous_frames("BALL_FRAME", ball_frames);

    let mut generated = String::from("// @generated by build.rs from umlang-sprites.txt\n");
    write_frame_array(&mut generated, "PLAYER_FRAMES", &player_frames);
    generated.push('\n');
    write_frame_array(&mut generated, "BALL_FRAMES", &ball_frames);

    let out_path = PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set"))
        .join("generated_sprites.rs");
    fs::write(out_path, generated).expect("failed to write generated sprite frames");
}

fn generate_animation() {
    println!("cargo:rerun-if-changed=umlang-animation.txt");

    let values = read_key_values("umlang-animation.txt");
    let mut anim_rules = BTreeMap::new();
    let mut draw_states = BTreeMap::new();

    for (key, value) in values {
        if let Some(index) = key.strip_prefix("player_anim_state_") {
            let state = parse_frame_index("player_anim_state", index) as i32;
            let values = parse_i32_list_value(&key, &value);
            assert!(
                values.len() == 2,
                "{key} must be frame_counter_divisor,frame_modulo"
            );
            assert!(
                values[0] > 0 && values[1] > 0,
                "{key} values must be positive"
            );
            assert!(
                anim_rules.insert(state, [values[0], values[1]]).is_none(),
                "duplicate player anim state: {state}"
            );
        } else if let Some(index) = key.strip_prefix("player_draw_state_") {
            let state = parse_frame_index("player_draw_state", index) as i32;
            let values = parse_i32_list_value(&key, &value);
            assert!(
                !values.is_empty(),
                "{key} must list at least one sprite index"
            );
            assert!(
                values.iter().all(|value| *value >= 0),
                "{key} sprite indices must be non-negative"
            );
            assert!(
                draw_states.insert(state, values).is_none(),
                "duplicate player draw state: {state}"
            );
        } else {
            panic!("unknown animation key: {key}");
        }
    }

    assert!(
        !anim_rules.is_empty(),
        "umlang-animation.txt must define player animation states"
    );
    assert!(
        !draw_states.is_empty(),
        "umlang-animation.txt must define player draw states"
    );

    let mut generated = String::from("// @generated by build.rs from umlang-animation.txt\n");
    generated.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq)]\n");
    generated.push_str("pub struct PlayerAnimRule {\n");
    generated.push_str("    pub state: i32,\n");
    generated.push_str("    pub frame_divisor: i32,\n");
    generated.push_str("    pub frame_modulo: i32,\n");
    generated.push_str("}\n\n");
    generated.push_str("#[derive(Debug, Clone, Copy, PartialEq, Eq)]\n");
    generated.push_str("pub struct PlayerDrawState {\n");
    generated.push_str("    pub state: i32,\n");
    generated.push_str("    pub sprite_indices: &'static [usize],\n");
    generated.push_str("}\n\n");

    generated.push_str("pub const PLAYER_ANIM_RULES: &[PlayerAnimRule] = &[\n");
    for (state, [frame_divisor, frame_modulo]) in &anim_rules {
        generated.push_str(&format!(
            "    PlayerAnimRule {{ state: {state}, frame_divisor: {frame_divisor}, frame_modulo: {frame_modulo} }},\n"
        ));
    }
    generated.push_str("];\n\n");

    for (state, sprite_indices) in &draw_states {
        generated.push_str(&format!(
            "pub const PLAYER_DRAW_STATE_{state}: &[usize] = &["
        ));
        for (index, sprite_index) in sprite_indices.iter().enumerate() {
            if index > 0 {
                generated.push_str(", ");
            }
            generated.push_str(&sprite_index.to_string());
        }
        generated.push_str("];\n");
    }
    generated.push_str("\npub const PLAYER_DRAW_STATES: &[PlayerDrawState] = &[\n");
    for state in draw_states.keys() {
        generated.push_str(&format!(
            "    PlayerDrawState {{ state: {state}, sprite_indices: PLAYER_DRAW_STATE_{state} }},\n"
        ));
    }
    generated.push_str("];\n");

    let out_path = PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set"))
        .join("generated_animation.rs");
    fs::write(out_path, generated).expect("failed to write generated animation constants");
}

fn generate_rng() {
    println!("cargo:rerun-if-changed=umlang-rng.txt");

    let values = read_key_values("umlang-rng.txt");
    let seed_bytes = required_byte_list(&values, "seed_bytes");
    let multiplier_bytes = required_byte_list(&values, "multiplier_bytes");
    assert!(
        seed_bytes.len() == 8,
        "seed_bytes must contain exactly 8 bytes"
    );
    assert!(
        multiplier_bytes.len() == 8,
        "multiplier_bytes must contain exactly 8 bytes"
    );

    let mut generated = String::from("// @generated by build.rs from umlang-rng.txt\n");
    write_i32_array(&mut generated, "RNG_SEED_BYTES", &seed_bytes);
    generated.push('\n');
    write_i32_array(&mut generated, "RNG_MULTIPLIER_BYTES", &multiplier_bytes);

    let out_path =
        PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set")).join("generated_rng.rs");
    fs::write(out_path, generated).expect("failed to write generated rng constants");
}

fn generate_render() {
    println!("cargo:rerun-if-changed=umlang-render.txt");

    let values = read_key_values("umlang-render.txt");
    let mut generated = String::from("// @generated by build.rs from umlang-render.txt\n");
    for (key, value) in values {
        let const_name = key.to_ascii_uppercase();
        let values = parse_i32_list_value(&key, &value);
        assert!(!values.is_empty(), "render key {key} must not be empty");
        write_i32_array(&mut generated, &const_name, &values);
    }

    let out_path =
        PathBuf::from(env::var("OUT_DIR").expect("OUT_DIR is not set")).join("generated_render.rs");
    fs::write(out_path, generated).expect("failed to write generated render constants");
}

fn read_var_name_set() -> BTreeSet<String> {
    let text = fs::read_to_string("umlang-vars.txt").expect("failed to read umlang-vars.txt");
    let mut names = BTreeSet::new();
    for raw_line in text.lines() {
        let line = strip_comment(raw_line);
        if line.is_empty() {
            continue;
        }
        let (name, _) = line
            .split_once('=')
            .unwrap_or_else(|| panic!("invalid variable slot line: {line}"));
        let name = name.trim();
        validate_const_name(name, "");
        names.insert(name.to_string());
    }
    names
}

fn parse_sfx_event(
    key: &str,
    value: &str,
    valid_vars: &BTreeSet<String>,
    sfx_sound_count: usize,
) -> (String, i32, String) {
    let fields: Vec<_> = value.split(',').map(str::trim).collect();
    assert!(
        fields.len() == 3,
        "{key} must be flag_variable,sound_id,side"
    );
    let flag_name = fields[0];
    validate_const_name(flag_name, "");
    assert!(
        valid_vars.contains(flag_name),
        "{key} references unknown variable flag: {flag_name}"
    );
    let sound_id = parse_sfx_sound_id(key, fields[1], sfx_sound_count);
    let side = parse_sfx_side(key, fields[2]);
    (flag_name.to_string(), sound_id, side)
}

fn parse_sfx_sound_id(key: &str, value: &str, sfx_sound_count: usize) -> i32 {
    let sound_id = value
        .parse::<i32>()
        .unwrap_or_else(|_| panic!("invalid sound id for {key}: {value}"));
    assert!(
        sound_id > 0 && sound_id <= sfx_sound_count as i32,
        "{key} sound id {sound_id} must be within the SFX wave bank"
    );
    sound_id
}

fn parse_fixed_sfx_side(key: &str, value: &str) -> i32 {
    let side = value
        .parse::<i32>()
        .unwrap_or_else(|_| panic!("invalid fixed SFX side for {key}: {value}"));
    assert!(
        (-1..=1).contains(&side),
        "{key} fixed SFX side must be -1, 0, or 1"
    );
    side
}

fn parse_sfx_side(key: &str, value: &str) -> String {
    if value == "ball_x" {
        return "SfxSide::BallX".to_string();
    }
    format!("SfxSide::Fixed({})", parse_fixed_sfx_side(key, value))
}

fn write_sfx_events(generated: &mut String, name: &str, events: &[(String, i32, String)]) {
    generated.push_str(&format!("pub const {name}: &[SfxEvent] = &[\n"));
    for (flag_name, sound_id, side) in events {
        generated.push_str(&format!(
            "    SfxEvent {{ flag_name: {:?}, sound_id: {sound_id}, side: {side} }},\n",
            flag_name
        ));
    }
    generated.push_str("];\n");
}

fn parse_sprite_rect(name: &str, value: &str) -> [i32; 4] {
    let values = parse_i32_list_value(name, value);
    assert!(
        values.len() == 4,
        "sprite frame {name} must have exactly 4 values"
    );
    [values[0], values[1], values[2], values[3]]
}

fn parse_frame_index(kind: &str, value: &str) -> usize {
    value
        .parse()
        .unwrap_or_else(|_| panic!("invalid {kind} index: {value}"))
}

fn contiguous_frames(kind: &str, frames: BTreeMap<usize, [i32; 4]>) -> Vec<[i32; 4]> {
    assert!(!frames.is_empty(), "{kind} must define at least one frame");
    let mut values = Vec::new();
    for (expected, (index, frame)) in frames.into_iter().enumerate() {
        assert!(
            index == expected,
            "{kind} frame indices must be contiguous from 0; missing {expected}"
        );
        values.push(frame);
    }
    values
}

fn write_frame_array(generated: &mut String, name: &str, frames: &[[i32; 4]]) {
    generated.push_str(&format!("pub const {name}: &[[i32; 4]] = &[\n"));
    for [sx, sy, sw, sh] in frames {
        generated.push_str(&format!("    [{sx}, {sy}, {sw}, {sh}],\n"));
    }
    generated.push_str("];\n");
}

fn read_key_values(path: &str) -> BTreeMap<String, String> {
    let text =
        fs::read_to_string(path).unwrap_or_else(|err| panic!("failed to read {path}: {err}"));
    let mut values = BTreeMap::new();
    for raw_line in text.lines() {
        let line = strip_comment(raw_line);
        if line.is_empty() {
            continue;
        }
        let (key, value) = line
            .split_once('=')
            .unwrap_or_else(|| panic!("invalid key/value line in {path}: {line}"));
        let key = key.trim();
        assert!(
            key.chars()
                .all(|ch| ch.is_ascii_lowercase() || ch.is_ascii_digit() || ch == '_'),
            "invalid key in {path}: {key}"
        );
        assert!(
            values
                .insert(key.to_string(), value.trim().to_string())
                .is_none(),
            "duplicate key in {path}: {key}"
        );
    }
    values
}

fn required_value<'a>(values: &'a BTreeMap<String, String>, key: &str) -> &'a str {
    values
        .get(key)
        .map(String::as_str)
        .filter(|value| !value.is_empty())
        .unwrap_or_else(|| panic!("missing required asset key: {key}"))
}

fn required_i32(values: &BTreeMap<String, String>, key: &str) -> i32 {
    required_value(values, key)
        .parse()
        .unwrap_or_else(|_| panic!("invalid i32 asset value for {key}"))
}

fn required_i32_list(values: &BTreeMap<String, String>, key: &str) -> Vec<i32> {
    let values = parse_i32_list_value(key, required_value(values, key));
    assert!(!values.is_empty(), "empty i32 list asset value for {key}");
    values
}

fn required_byte_list(values: &BTreeMap<String, String>, key: &str) -> Vec<i32> {
    let values = required_i32_list(values, key);
    for value in &values {
        assert!(
            (0..=255).contains(value),
            "{key} values must be bytes, got {value}"
        );
    }
    values
}

fn parse_i32_list_value(context: &str, value: &str) -> Vec<i32> {
    parse_i32_delimited_value(context, value, ',')
}

fn parse_i32_delimited_value(context: &str, value: &str, delimiter: char) -> Vec<i32> {
    value
        .split(delimiter)
        .map(str::trim)
        .filter(|value| !value.is_empty())
        .map(|value| {
            value
                .parse()
                .unwrap_or_else(|_| panic!("invalid i32 list item for {context}: {value}"))
        })
        .collect()
}

fn strip_comment(line: &str) -> &str {
    line.split_once('#')
        .map_or(line, |(before, _)| before)
        .trim()
}

fn validate_const_name(name: &str, prefix: &str) {
    assert!(
        name.starts_with(prefix)
            && name
                .chars()
                .all(|ch| ch.is_ascii_uppercase() || ch.is_ascii_digit() || ch == '_'),
        "invalid constant name: {name}"
    );
}

fn write_i32_array(generated: &mut String, name: &str, values: &[i32]) {
    generated.push_str(&format!("pub const {name}: &[i32] = &["));
    for (index, value) in values.iter().enumerate() {
        if index > 0 {
            generated.push_str(", ");
        }
        generated.push_str(&value.to_string());
    }
    generated.push_str("];\n");
}
